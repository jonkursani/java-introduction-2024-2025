package java_03.ushtrime;

public class VariablesUshtrime {
    public static void main(String[] args) {
        // Detyra 2
        double width = 5.6, height = 8.5;

        System.out.printf("Syprina eshte %.1f * %.1f = %.2f.%n", width, height, (width * height));
        System.out.printf("Perimetri eshte 2 * (%.1f + %.1f) = %.2f.%n", width, height, 2 * (width + height));
    }
}
